#  can store int , float, string or any type of data in a single list
data=["suraj","tcs",1.9,1,True]

print(data) # ['suraj', 'tcs', 1.9, 1, True]


print(type(data)) # <class 'list'>

print(type(data[0]))  #<class 'str'>


print(type(data[2])) # <class 'float'>

print(type(data[4])) # <class 'bool'>